use anyhow::Result;
use clap::{Parser, Subcommand};

mod scripts;

use scripts::split;

/// A binary splitting tool to assist with decompilation and modding projects

#[derive(Clone, Debug, Subcommand)]
enum Commands {
    Split {
        #[clap(flatten)]
        args: split::SplitArgs,
    },
    CreateConfig,
    Capy,
}

#[derive(Parser, Debug)]
#[command(version, about, long_about = None)]
struct Args {
    /// Command to run
    #[command(subcommand)]
    command: Commands,
}

fn main() -> Result<()> {
    let args = Args::parse();

    match args.command {
        Commands::Split { args } => {
            println!("Splitting");
            args.do_stuff()?;
        }
        Commands::CreateConfig => {
            println!("Creating config");
        }
        Commands::Capy => capybara(),
    }

    Ok(())
}

